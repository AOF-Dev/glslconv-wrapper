static ShHandle mVertexCompiler = NULL;
static ShHandle mFragmentCompiler = NULL;
static int mOptions = EOptionIntermediate;

extern "C" int glslconv_init() {
    ShInitialize();
    int Options = EOptionIntermediate;
    mVertexCompiler = ShConstructCompiler(EShLangVertex, mOptions);
    mFragmentCompiler = ShConstructCompiler(EShLangFragment, mOptions);
    return mVertexCompiler != NULL && mFragmentCompiler != NULL;
}

extern "C" int glslconv_fini() {
    ShDestruct(mVertexCompiler);
    ShDestruct(mFragmentCompiler);
    ShFinalize();
    return 0;
}

extern "C" char* glslconv_conv(const char* shader_string, int type) {
    char* tmp = NULL;
    int ret = 0;
    EShMessages messages = EShMsgConvert;
    TBuiltInResource Resources = glslang::DefaultTBuiltInResource;
    std::string result = "";
    
    if (type == 1) {
        ret = ShCompile(mFragmentCompiler, &shader_string, 1, nullptr, EShOptNone, &Resources, mOptions, 100, false, messages);
        result = ShGetInfoLog(mFragmentCompiler);
        tmp = strdup(result.c_str());
        if (strncmp(tmp, "ERROR:", 6) != 0) {
            return tmp;
        }
    }
    else {
        ret = ShCompile(mVertexCompiler, &shader_string, 1, nullptr, EShOptNone, &Resources, mOptions, 100, false, messages);
        result = ShGetInfoLog(mVertexCompiler);
        tmp = strdup(result.c_str());
        if (strncmp(tmp, "ERROR:", 6) != 0) {
            return tmp;
        }
    }
    
    free(tmp);
    return NULL;
}

